    private void registrarInscripcionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarInscripcionBtnActionPerformed
        int filaAlumno = this.alumnosJTable.getSelectedRow();
        int alumnoId = Integer.valueOf(this.alumnosJTable.getValueAt(filaAlumno, 0).toString());
        
        int filaClase = this.clasesJTable.getSelectedRow();
        int claseId = Integer.valueOf(this.clasesJTable.getValueAt(filaClase, 0).toString());
        
        Inscripcion inscripcion = new Inscripcion();
        inscripcion.setAlumnoId(alumnoId);
        inscripcion.setClaseId(claseId);
        inscripcion.setCreated_at(LocalDateTime.now());
        inscripcion.save(inscripcion);
        
        this.mostrarInscripciones();
    }//GEN-LAST:event_registrarInscripcionBtnActionPerformed

