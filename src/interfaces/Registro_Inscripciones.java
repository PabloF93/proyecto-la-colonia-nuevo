    private void mostrarAlumnos() {
        DefaultTableModel modelo = (DefaultTableModel) this.alumnosJTable.getModel();
        int columnas = modelo.getColumnCount();
        modelo.setRowCount(0);
        
        rs = new Alumno().list();
        
        try {
            while(rs.next()) {
                Object fila[] = new Object[columnas];
                
                fila[0] = rs.getInt("id");
                fila[1] = rs.getString("dni");
                fila[2] = rs.getString("nombreCompleto");
                
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
    private void mostrarClases() {
        DefaultTableModel modelo = (DefaultTableModel) this.clasesJTable.getModel();
        int columnas = modelo.getColumnCount();
        modelo.setRowCount(0);
        
        rs = new Clase().tableView1();
        
        try {
            while(rs.next()) {
                Object fila[] = new Object[columnas];
                fila[0] = rs.getObject(1);
                fila[1] = rs.getObject(2);
                fila[2] = rs.getObject(3);
                fila[3] = rs.getObject(4);
                fila[4] = rs.getObject(6);
                fila[5] = rs.getObject(5);
                
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void mostrarInscripciones() {
        DefaultTableModel modelo = (DefaultTableModel) this.inscripcionesJTable.getModel();
        int columnas = modelo.getColumnCount();
        modelo.setRowCount(0);
        
        rs = new Inscripcion().tableView2();
        
        try {
            while(rs.next()) {
                Object fila[] = new Object[columnas];
                for(int i=0; i < fila.length; i++) {
                    fila[i] = rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

